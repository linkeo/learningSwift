let one = "+ "
let zero = "  "

func cell(total: Int) -> [Int] {
	var cells = [Int]()
	for i in 2...total/2 {
		if (total-1)%i == 0 {
			cells.append(i)
		}
	}
	return cells
}

func divideIntoRooms(#x: Int, #y: Int, atLeast: Int = 0, minSize: Int = 6, minWidth: Int = 2, minHeight: Int = 2 ) {
	let xcells = cell(x)
	let ycells = cell(y)
	if xcells.isEmpty || xcells.isEmpty {
		println("can't divide \(x)x\(y) blocks into rooms")
	} else {
		var caseCount = 0
		for xcell in xcells {
			for ycell in ycells {
				let rooms = (x-1)/xcell * (y-1)/ycell
				let w = xcell-1
				let h = ycell-1
				if w*h < minSize || rooms < atLeast || w < minWidth || h < minHeight { continue }
				println("Case \(++caseCount)\nDivide \(x)x\(y) blocks into \(xcell-1)x\(ycell-1) rooms(\(rooms)).")
				for j in 0..<y {
					var str = ""
					if j % ycell == 0 {
						for i in 0..<x {
							str += one
						}
					} else {
						for i in 0..<x {
							if i % xcell == 0 {
								str += one
							} else {
								str += zero
							}
						}
					}
					println(str)
				}
			}
		}
	}
}

